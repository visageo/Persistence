    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        try (java.sql.Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/ormassignment", "root", "");) {

            PreparedStatement st = conn.prepareStatement("insert into employee (name,age,salary) values (?,?,?)");
            st.setString(1, textFieldName.getText());
            st.setString(2, textFieldAge.getText());
            st.setString(3, textFieldSalary.getText());
            st.execute();
            outputArea.setText("Employee " + textFieldName.getText() + " added");

        } catch (SQLException ex) {
            System.out.println("Error in database connection: \n" + ex.getMessage());
            outputArea.setText("Error in database connection: \n" + ex.getMessage());
        }
    }//GEN-LAST:event_buttonAddActionPerformed

