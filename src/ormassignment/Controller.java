    private void buttonShowAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonShowAllActionPerformed
        try (java.sql.Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/ormassignment", "root", "");) {
            Employee e = null;
            List<Employee> employee_collection = new ArrayList();

            Statement st = conn.createStatement();
            st.executeQuery("select * from employee");
            ResultSet rs = st.getResultSet();
            outputArea.setText("Showing all employees: \n\n");
            while (rs.next()) {

                e = new Employee(rs.getString("name"), rs.getInt("age"), rs.getInt("salary"));
                employee_collection.add(e);
                outputArea.append("Employee: " + e.getName() + ", age: " + e.getAge() + ", earns: " + e.getSalary() + "\n");
            }

        } catch (SQLException ex) {
            System.out.println("Error in database connection: \n" + ex.getMessage());
            outputArea.setText("Error in database connection: \n" + ex.getMessage());
        }
    }//GEN-LAST:event_buttonShowAllActionPerformed

